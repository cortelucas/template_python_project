def soma_dois_numeros(number_01: float, number_02: float) -> float:
    """## Soma dois números

    Params:
        - number_01 -- primeiro número real para a soma
        - number_02 -- segundo número real para a soma

    Return:
        - Soma entre dois números
    """
    soma = number_01 + number_02
    return soma


print(soma_dois_numeros(1.2, 8.9))
